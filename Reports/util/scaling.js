export function scaleScore(score, type, activity,locale) {
  if(scaleType[type]) {
    let metrics = getRolledUpActivityMetrics(activity);
    if(metrics == {}) return null;
    if(type !== "WCPM"){
      //###Leave in for debug console.log(activity,metrics);
      let CLSScore = calculateCLS(metrics.areaScore,score,metrics.wordsRead,metrics.numberOfErrors,activity.student.grade,activity.createdAt,locale);
      //###Leave in for debug console.log(activity.activityId,CLSScore)
      let concordanceScore = doConcordance(CLSScore,locale);
      return concordanceScore[type];
    }else{
      return Math.round(activity.scores.wcpmScore);
    }

  } else {
    console.log('error, unknown scale type: ', type);
    return null;
  }
}

export function scaleORF(score, sourceType, translateType) {
  if(scaleType[sourceType] && scaleType[translateType]) {
    let roundedScore = Math.round(score);
    let concordanceScore = wcpmLookupTable.find(s => s[sourceType] === roundedScore);
    return concordanceScore && concordanceScore[translateType] || 0;
  } else {
    console.log('error, unknown scale type: ', sourceType,translateType);
    return null;
  }
}

export function calculateCLS(areaScore,adjustedWCPM,wordsRead,numberOfErrors,studentGrade,activityDate,locale){
  let baseScore = calculateBaseScore(wordsRead,numberOfErrors,studentGrade,activityDate);
  let areaFromWCPM = wcpmToArea(adjustedWCPM,studentGrade,activityDate,locale);
  //###Leave in for debug console.log("CLS:",activityDate)
  //###Leave in for debug console.log("CLS",areaScore,areaFromWCPM,baseScore);
  let CLS = 5.2;
  if(wordsRead-numberOfErrors>14){
    CLS = Math.max(5.3,((3*areaScore)+areaFromWCPM+baseScore)/5);
  }

  let minmax = getBaseMinMax(studentGrade,activityDate);
  if(CLS > minmax.max) CLS = minmax.max;

  return CLS;
}

export function calculateBaseScore(wordsRead,numberOfErrors,grade,date){
  //###Leave in for debug console.log("********************")
  //###Leave in for debug console.log(wordsRead,numberOfErrors,grade);
  let coreBaseScore = (((((wordsRead/200)*100)+((1-(1.2*(numberOfErrors/wordsRead)))*100)))/17)-1;

  //###Leave in for debug console.log("CBS",coreBaseScore);
  //Adjust for short forms
  let sfAdjustment = 0;
  if((wordsRead<60)&&(wordsRead-numberOfErrors>20)){
    sfAdjustment = 0.7;
  }else{
    if((coreBaseScore<6.5) && (wordsRead-numberOfErrors>20)){
      sfAdjustment = 0.9;
    }else{
      if((coreBaseScore>9)&&(numberOfErrors>20)){
        sfAdjustment = -0.8;
      }else{
        if(coreBaseScore>9){
          sfAdjustment = -0.9;
        }
      }
    }
  }
  //###Leave in for debug console.log("SFA",sfAdjustment);

  //Adjust for errors
  let errAdjustment = 0;
  //###Leave in for debug console.log("RATIO",numberOfErrors/wordsRead);
  if(numberOfErrors/wordsRead>0.24){
    errAdjustment = -0.5;
  }else{
    if(numberOfErrors/wordsRead>0.16){
      errAdjustment = -0.3;
    }else{
      if(numberOfErrors/wordsRead<0.05){
        errAdjustment = 0.3;
      }
    }
  }


  //Adjust the core Base Score
  //###Leave in for debug //###Leave in for debug console.log("ERRA",errAdjustment);
  let baseScore = coreBaseScore + errAdjustment + sfAdjustment;
  //###Leave in for debug console.log("BS1",baseScore);
  //Now adjust for level
  if(baseScore>8.5){
    baseScore -= 0.6;
  }else{
    if(baseScore>7){
      baseScore -= 0.35;
    }else{
      baseScore -= 0.2;
    }
  }
  //###Leave in for debug console.log("BS",baseScore)
  //Apply the min and max
  //###Leave in for debug console.log("G",grade);
  let minmax = getBaseMinMax(grade,date);
  //###Leave in for debug console.log("BS:",date)
  //###Leave in for debug console.log("MM",minmax);
  //###Leave in for debug console.log(minmax.max,baseScore,minmax.min);
  baseScore = Math.min(minmax.max,Math.max(baseScore,minmax.min));

  return baseScore;
}

function getBaseMinMax(grade,date){
  let season = getSeason(date);
  let seasonMinMax = baseScoreMinMax.filter((row)=>{
    return row.season == season;
  });
  let baseMinMax = seasonMinMax.filter((row)=>{
    return row.grade == grade;
  });

  return baseMinMax[0];
}

function wcpmToArea(wcpmScore,grade,date,locale){
  const isValueLargerThanScore = (element) => element[0] > wcpmScore;
  let tableToUse = locale === locales.ES_MX ? spanishWcpmToAreaLookup : wcpmToAreaLookup;
  let index = tableToUse.findIndex(isValueLargerThanScore) - 1;

  if(index < 0) index = tableToUse.length - 1;

  let areaLookup = tableToUse[index][1];
  //###Leave in for debug console.log("WtoALookup",areaLookup)
  if(areaLookup > 6.3) areaLookup -= 0.7;
  //###Leave in for debug console.log("AdjWtoA",areaLookup)
  let minmax = getBaseMinMax(grade,date);
  let area = Math.min(minmax.max,Math.max(areaLookup,minmax.min));
  //###Leave in for debug console.log("finalWtoA",area);
  return(area);
}

