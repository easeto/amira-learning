export function getRolledUpActivityMetrics(activity){
  let baseActivityMetrics = getActivityMetrics(activity);
  if(activity.children){
    activity.children.map((child)=>{
      if(child.type == 'readingComprehension'){
        let childMetrics = getActivityMetrics(child);
        baseActivityMetrics.wordsRead += childMetrics.wordsRead;
        baseActivityMetrics.numberOfErrors += childMetrics.numberOfErrors;
      }
    });
  }

  return baseActivityMetrics;
}

export function getActivityMetrics(activity){
  if(!activity.errors){
    return {};
  }

  let wordsRead = activity.errors.reduce((wordsSoFar,phraseErrors)=>{
    return phraseErrors.length + wordsSoFar;
  },0);

  let numberOfErrors = activity.errors.reduce((errorsSoFar,phraseErrors)=>{
    let errors = phraseErrors.reduce((accum,error)=>{
      return accum + (error ? 1 : 0);
    },0);
    return errorsSoFar + errors;
  },0);

  let score = activity.scores && activity.scores.areaScore;
  return {wordsRead: wordsRead, numberOfErrors: numberOfErrors, areaScore: score};
}

